-- Создание таблицы "Товары"
CREATE TABLE Товары (
  Идентификатор INT PRIMARY KEY AUTO_INCREMENT,
  Наименование VARCHAR(255),
  Количество INT,
  Доступность BOOLEAN
);

-- Создание таблицы "Склады"
CREATE TABLE Склады (
  Идентификатор INT PRIMARY KEY AUTO_INCREMENT,
  Наименование VARCHAR(255),
  Адрес VARCHAR(255),
  Другие_данные VARCHAR(255)
);

-- Создание таблицы "Движение товаров"
CREATE TABLE Движение_товаров (
  Идентификатор INT PRIMARY KEY AUTO_INCREMENT,
  Идентификатор_товара INT,
  Идентификатор_склада INT,
  Количество INT,
  Дата_движения DATE,
  FOREIGN KEY (Идентификатор_товара) REFERENCES Товары(Идентификатор),
  FOREIGN KEY (Идентификатор_склада) REFERENCES Склады(Идентификатор)
);
