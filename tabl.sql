-- Удаление товара
DELETE FROM Товары
WHERE Идентификатор = 1;
-- Перемещение товара на другой склад
UPDATE Товары
SET Количество = Количество - 5
WHERE Идентификатор = 1;

-- Обновление доступности товара
UPDATE Товары
SET Доступность = false
WHERE Количество = 0;
SELECT Товары.Наименование, Товары.Количество
FROM Товары
JOIN Движение_товаров ON Товары.Идентификатор = Движение_товаров.Идентификатор_товара
WHERE Движение_товаров.Идентификатор_склада = 1;
